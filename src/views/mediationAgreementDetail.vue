          <div class="stage">1．确定可取得拆迁利益主体的同住人资格 根据相关规定，在涉及公有房屋拆迁补偿纠纷中，对于同住人的界定，为拆迁许可证核发之日，在被拆迁居住房屋处有本市常住户口，已实际居住1年以上，且本市无其他住房或者虽有其他住房但居住困难的人。这里的他处虽有住房但居住困难的情况，是指他处房屋内人均居住面积不足法定最低标准的情况，他处房屋的性质仅限于福利性质取得的房屋。此外还要考虑三种情况：一是在公房内居住的未成年人的问题。一般而言，允许未成年人在自己承租的公房内居住的，可认定为帮助性质，除另有约定外，并不当然等于同意该未成年人取得房屋的权利份额。因此，该未成年人无权主张分割房屋拆迁补偿款，除非其能提供证据证明自己的居住权并非基于他人的帮助而取得。二是返城知青能否成为同住人的问题。要考虑家庭实际状况，除双方有协议外，一般情况下知青可成为同住人分割补偿款。三是外来人员、参军人员和服刑人员能否作为同住人的问题。根据有关规定，具有本市常住户口，至拆迁许可证核发之日，因结婚而在被拆迁公有住房内居住的，即使居住未满1年的；或在本市无常住户口，至拆迁许可证核发之日，因结婚而在被拆迁公有住房内居住满5年的；或房屋拆迁时，因在服兵役、读大学、服刑等原因户籍被迁出被拆公有居住房屋，且在本市他处没有福利性房屋的，均可视为同住人。 2．审查拆迁资料并听取拆迁单位意见 由于现实情况纷繁复杂，确定应安置人员存在一定的难度，动拆迁单位在实践中对动拆迁补偿款分割人员的核定有三种形式：一是动拆迁单位和被拆迁人（以户为单位）签订的《房屋动拆迁安置协议》中附有安置人员的名单，除非上述条款被法院宣告无效，一般都按该协议分割动拆迁补偿款；二是在《房屋动拆迁安置协议》中无相关条款，但在动拆迁单位动迁之初以户为单位制定的“两定表”（即核定人员表、核定面积表）中有相关记载的，一般以该表中的相关内容为参考，依据当事人提出的合理合法的主张，确定安置补偿人员；三是当《房屋动拆迁安置协议》及“两定表”对相关安置人员均不明晰时，调解人员可至动拆迁单位了解具体情况，参考其意见合理确定安置人员。</div>
